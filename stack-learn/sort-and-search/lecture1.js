// Implement Bubble Sort
let myArr = [1, 3, 54, 78, 90, 41, 5];

// Ascending Order
const bubbleSort = (arr) => {
  for (let i = 0; i < arr.length - 1; i++) {
    for (let j = 0; j < arr.length - 1; j++) {
      if (arr[j] > arr[j + 1]) {
        [arr[j], arr[j + 1]] = [arr[j + 1], arr[j]]; // Swap in one line
      }
    }
  }
};
bubbleSort(myArr);
console.log(myArr);

